#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*l;

	l = malloc(sizeof(t_list));
	if (!l)
		return (NULL);
	if (l != NULL)
	{
		l->content = content;
		l->next = NULL;
	}
	return (l);
}
